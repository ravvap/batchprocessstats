-- =============================================================================
--  V1 — Initial schema: batch_source_system, batch_job_history
--
--  Two tables back the BPS module:
--    batch_source_system  — reference table; seeded with SIMS, RRPS, CBIS
--    batch_job_history    — primary operational table; FK → batch_source_system
--
--  Roles are managed entirely via Microsoft Entra ID application permissions
--  (BATCH_PRCS_STATS_VIEW, BATCH_PRCS_STATS_ADD, BATCH_PRCS_STATS_EDIT).
--  No role table exists in the database.
--
--  batch_processing_statistics does NOT exist — batch_job_history is the
--  single backing table for the BPS module.
-- =============================================================================

-- ---------------------------------------------------------------------------
-- 1. batch_source_system
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS batch_source_system (
    id           bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY
                     ( INCREMENT 1 START 1 MINVALUE 1
                       MAXVALUE 9223372036854775807 CACHE 1 ),
    source_name  character varying(100) COLLATE pg_catalog."default" NOT NULL,
    description  character varying(500) COLLATE pg_catalog."default",
    channel_type character varying(100) COLLATE pg_catalog."default",
    status       character varying(100) COLLATE pg_catalog."default",
    CONSTRAINT batch_source_system_pkey PRIMARY KEY (id)
);

-- Seed data (SIMS, RRPS, CBIS)
INSERT INTO batch_source_system (source_name, description, channel_type, status)
VALUES
    ('SIMS', NULL, NULL, NULL),
    ('RRPS', NULL, NULL, NULL),
    ('CBIS', NULL, NULL, NULL)
ON CONFLICT DO NOTHING;

-- ---------------------------------------------------------------------------
-- 2. batch_job_history  — primary BPS backing table
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS batch_job_history (
    id                               bigint  NOT NULL GENERATED BY DEFAULT AS IDENTITY
                                                 ( INCREMENT 1 START 1 MINVALUE 1
                                                   MAXVALUE 9223372036854775807 CACHE 1 ),
    -- Audit columns (server-managed — never accepted in POST/PUT request bodies,
    -- always returned in every API response per BPS-010)
    created_by                       character varying(100) NOT NULL,
    created_date_time                timestamp with time zone NOT NULL DEFAULT now(),
    updated_by                       character varying(100) NOT NULL,
    updated_date_time                timestamp with time zone NOT NULL DEFAULT now(),
    -- Operational fields
    job_id                           bigint,
    job_type                         character varying(100) COLLATE pg_catalog."default"
                                         NOT NULL DEFAULT '',
    batch_type                       character varying(20)  COLLATE pg_catalog."default"
                                         CHECK (batch_type IN ('EMAIL','API','SFTP_PULL','BATCH')),
    retry_count                      integer,
    job_status                       character varying(50)  COLLATE pg_catalog."default"
                                         NOT NULL DEFAULT '',
    start_time                       timestamp with time zone,
    end_time                         timestamp with time zone,
    status                           character varying(100) COLLATE pg_catalog."default"
                                         NOT NULL DEFAULT '',
    error_message                    character varying(500) COLLATE pg_catalog."default",
    source_system_id                 bigint NOT NULL,
    records_changed                  integer,
    records_gathered                 integer,
    records_processed_current_period integer,
    records_processed_prior_period   integer,
    records_unpostable               integer,
    CONSTRAINT batch_job_history_pkey PRIMARY KEY (id),
    CONSTRAINT fk_batch_job_history_source_system
        FOREIGN KEY (source_system_id)
        REFERENCES batch_source_system (id)
);

-- Indexes
CREATE INDEX IF NOT EXISTS idx_batch_job_history_source_system_start_time
    ON batch_job_history (source_system_id, start_time DESC);

CREATE INDEX IF NOT EXISTS idx_batch_job_history_job_status
    ON batch_job_history (job_status);

CREATE INDEX IF NOT EXISTS idx_batch_job_history_batch_type
    ON batch_job_history (batch_type);

CREATE INDEX IF NOT EXISTS idx_batch_job_history_job_type_lower
    ON batch_job_history (LOWER(job_type));
